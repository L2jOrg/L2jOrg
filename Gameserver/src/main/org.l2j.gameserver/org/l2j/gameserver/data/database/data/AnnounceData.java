package org.l2j.gameserver.data.database.data;

import org.l2j.commons.database.annotation.Table;
import org.l2j.gameserver.data.database.announce.Announce;
import org.l2j.gameserver.data.database.announce.AnnouncementType;

/**
 * @author JoeAlisson
 */
@Table(value = "announcements", autoGeneratedProperty = "id")
public class AnnounceData implements Announce {

    private int id;
    private AnnouncementType type;
    private long initial;
    private long delay;
    private int repeat;
    private String author;
    private String content;

    public AnnounceData() {

    }

    public AnnounceData(AnnouncementType type, String content, String author, long initDelay, long delay, int repeat) {
        this.type = type;
        this.content = content;
        this.author = author;
        this.initial = initDelay;
        this.delay = delay;
        this.repeat = repeat;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public AnnouncementType getType() {
        return type;
    }

    public void setType(AnnouncementType type) {
        this.type = type;
    }

    public long getInitial() {
        return initial;
    }

    public void setInitial(long initial) {
        this.initial = initial;
    }

    public long getDelay() {
        return delay;
    }

    public void setDelay(long delay) {
        this.delay = delay;
    }

    public int getRepeat() {
        return repeat;
    }

    public void setRepeat(int repeat) {
        this.repeat = repeat;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    @Override
    public boolean isValid() {
        return true;
    }

    @Override
    public boolean canBeStored() {
        return true;
    }
}
