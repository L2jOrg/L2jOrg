package org.l2j.gameserver.data.database.data;

import org.l2j.commons.database.annotation.Table;
import org.l2j.gameserver.model.punishment.PunishmentAffect;
import org.l2j.gameserver.model.punishment.PunishmentType;

/**
 * @author JoeAlisson
 */
@Table(value = "punishments", autoGeneratedProperty = "id")
public class PunishmentData {

    private int id;
    private String key;
    private PunishmentAffect affect;
    private PunishmentType type;
    private long expiration;
    private String reason;
    private String punisher;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public PunishmentAffect getAffect() {
        return affect;
    }

    public void setAffect(PunishmentAffect affect) {
        this.affect = affect;
    }

    public PunishmentType getType() {
        return type;
    }

    public void setType(PunishmentType type) {
        this.type = type;
    }

    public long getExpiration() {
        return expiration;
    }

    public void setExpiration(long expiration) {
        this.expiration = expiration;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getPunisher() {
        return punisher;
    }

    public void setPunisher(String punisher) {
        this.punisher = punisher;
    }

    public static PunishmentData of(String key, PunishmentAffect affect, PunishmentType type, long expiration, String reason, String punisher) {
        var data = new PunishmentData();
        data.key = key;
        data.affect = affect;
        data.type = type;
        data.expiration = expiration;
        data.reason = reason;
        data.punisher = punisher;
        return data;
    }
}
